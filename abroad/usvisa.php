<b>Visa's For Studying in  US</b><br>
<strong>*Procedures are fine tuned by the Embassy regularly. For latest updates feel free to get in touch with us.</strong>
<p>
To enter the United States as a student, you must apply at a US Embassy or Consulate for a student visa. You are required to make an appointment in India to apply for a visa. You can apply for a visa only after you receive the requisite I-20 or IAP-66 form from the institution you have been accepted to. The information outlined below is designed to help you understand the visa process.<br><br>

<b>Types of Visa:</b>

There are mainly two types of student visas:<br>
• F-1 (Student Visa). The F-1 visa is for full-time students enrolled in an academic or language program. F-1 students may stay in the US for the full length of their academic program plus 60 days. F-1 students must maintain a full-time course load and complete their studies by the expiration date listed on the I-20 form.<br><br>
• J-1 (Exchange Visitor Visa). The J-1 visa is issued for students needing practical training that is not available in their home country to complete their academic program. The training must be directly related to the academic program. The J-1 visa obligates the student to return to their home country for a minimum of two years after the end of their studies in the US before being eligible to apply for an immigrant (permanent residence) visa.
</p>
The rules and regulations governing the entrance of all international students into the United States are complicated and should be properly looked into before applying for a visa.<br><br>

<b>Visa Application Procedure:</b><br>
Applicants should apply at the consular section of the American Embassy any working day, Monday through Friday, between the hours of 8:30 and 10:00 AM.<br><br>

<pre><b>Documents required are:</b>
• Valid Passport
• One Passport size photograph (Black and White or Colour)
• A Non-immigrant Visa Application (Form 156) available from the Consular section.
• Form I-20 executed by the authorizing official of institution concerned, signed by the applicant.
• Evidence of financial ability to cover the costs of the education and maintenance involved
• Visa fee to be paid in Indian Rupees by Bank Draft</pre>
<br><br>
<b>When to apply:</b><br>
An applicant may apply for a student Visa not earlier than 90 days before the registration date specified on the FORM I-20. If the registration date is already passed or the applicant cannot reasonably expect to arrive at the school by the registration date, the applicant should obtain an amended I-20 or a letter of extension from the issuing institution stating by what date the applicant may arrive.<br><br>

<pre>Additional documents to be submitted:
• Your total personal annual income
• Total annual income of your Parents
• Your average annual living expenses
• Some details of your and your parents' assets
• Sources of funding for your education other than from the University</pre>
In most cases, the visa will be issued within a few hours or days of the submission of the application. In some instances, the process may take longer depending on the time of year, consular caseload or other factors.<br><br>

If your application is denied, you should ask the Consular Officer for a "written denial decision." This will provide you with the official reason for your visa denial.
