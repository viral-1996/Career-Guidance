<b>Tuition and Living Cost in Germany</b>
<p>
Although tuition fees in Germany were almost unheard of, it has changed in recent times. Few federal states charge around 500 Euros for each semester of undergraduate courses.<br>
Many German universities are state-sponsored in an effort to maintain affordable tuition fees. Besides, the number of scholarships also keeps increasing.<br><br>  
<b>Living costs:</b><br>
As compared to tuition expenses, living expenditure in Germany can be considerably high, rent being the major expense.  Sharing a flat for accommodation might be a reasonable option. Other options for accommodation include university residences. Although the costs vary from city to city, on an average, the living expenses can range from INR 10-12 lakhs per annum.<br>
The ‘Federal Education and Training Assistance Act’ was established by the German government to provide a minimum financial support to trainees. In Germany, scholarships and financial aidare available for all competent international students.</p>
