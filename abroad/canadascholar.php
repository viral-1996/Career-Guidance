<b>Scholarship's</b><br>
<p>
There are limited scholarships available in Canada but they rarely cover the full cost of a study program and they are not always easy to find.<br><br>

Scholarships to international students are available at some Canadian institutions for students with exceptional results. The amount awarded varies from one institution to another and usually covers only part of a student's education and living expenses.<br><br>

The calendar of each university in Canada includes a complete and detailed list of the scholarships it administers. This information is usually available on school web sites as well. Obtaining a scholarship is very competitive and applications must be made directly to each university along with the application for admission. Community and Career Colleges do not usually award scholarships nor do school boards or most private schools.<br><br>
</p>
