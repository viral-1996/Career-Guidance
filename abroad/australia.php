<b><h3><u>Country Overview Australia</h3></u></b>
Drenched in sunshine and glowing with affable charisma, Australia is an enticing land of vast beaches, pulsating cities and reams and reams of bushland. One of the country's greatest lures is its sense of space; a beach, patch of tropical forest or piece of sandy desert all to oneself is an easy reality. This, the world's largest island, encompasses a range of stunning landscapes, from barren deserts to tropical rainforests and rugged mountains.<br><br>

Australia has an abundance of unique animal life - cuddly koalas, bounding kangaroos and wallabies, darting dingoes, and ungainly emus - while the hulking form of Uluru (Ayers Rock) soaking up the reds and oranges of the outback's fiery sun, is the country's most iconic image.<br><br>

Glossy Sydney and arty Melbourne provide much for the city-lover; Brisbane is gateway to the tropical northeast with sandy stretches including Surfer's Paradise further south; restful Adelaide is perfect for a few days' rejuvenation, while Perth is young, brash and alluring; and Cairns is the stop-off point for one of Australia's top attractions, the Great Barrier Reef.<br><br>

<b>Culture and History:</b><br>
Captain Cook stumbled onto Australian shores in 1770 to find an Aboriginal way of life that went back some 40,000 years. By 1868, Britain had sent more than 160,000 convicts to Australia.<br><br>

Experiencing the culture of Australia's indigenous population is one of the great highlights of a visit. Many tensions still exist between mainstream Australia and its Aboriginal people. The first European settlers treated the Aboriginal population with appalling brutality, which gave way to racist and cruel policies from subsequent administrations. However, the slow march towards reconciliation was given a boost in 2008 when Prime Minister Kevin Rudd issued a formal apology to the indigenous population for the suffering of the past.<br><br>

In November 2007 elections, after almost 12 years of conservative rule, the public voted out the Liberal Party coalition and gave the Australian Labor Party (ALP) a decisive win. This has meant some significant changes in the direction of the country. Prime Minister Rudd swiftly set about signing the Kyoto Protocol, investing more in alternate energy sources.<br><br>

The Labor government's stimulus package and strong trading links with China have resulted in Australia riding out the 2008/2009 global financial crisis relatively unscathed. The government withdrew its troops from Iraq in early 2008, yet retains 1,500 troops in the Uruzgan province of Afghanistan.<br><br><br>

<b>Social Conventions:</b><br>
A largely informal atmosphere prevails; shaking hands is the customary greeting. Casual wear is worn everywhere except in the most exclusive restaurants, social gatherings and important business meetings. Most restaurants forbid smoking.<br>
