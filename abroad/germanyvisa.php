<b>Visa's For Studying in  Germany</b><br>
<strong>*Procedures are fine tuned by the Embassy regularly. For latest updates feel free to get in touch with us.</strong><br>
<p>
Generally, 2-10 working days are needed to process a visa for a short stay application whereas visas for a longer stay might take several months. Moreover, there may be a waiting period during the peak travel season. Hence, it is better to apply at the earliest possible.<br>
Study visas for Germany are valid for the duration of the study period, and the fee is around 60 Euro.<br><br>
<b>Documents required:</b><br>
– two fully completed application forms and required declaration in duplicate<br>
– two passport-sized photographs<br>
– a valid national passport and one copy<br>
– additional for non-US citizens: original of valid US alien registration card or valid US resident visa (type A, E, F, G, H, I, J, L, O, R) and one copy<br>
– letter of acceptance from a German university/language school/college and one copy<br>
– evidence that you have sufficient funds to support yourself while living in Germany and one copy<br><br>
