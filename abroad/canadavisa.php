<b>Visa's For Studying in Canada</b><br>
<strong>*Procedures are fine tuned by the Embassy regularly. For latest updates feel free to get in touch with us.</strong><br>
<p>
<b>Visa Procedure:</b><br>
International students are required to obtain a Study Permit for Canada. Allow at least a month for your visa to be processed.
Visa Procedure - Submit the completed application kit to the immigration section of the Canadian High commission, New Delhi. Kindly submit only notarized photocopies of all documents.<br><br>

Interview - The student may be called for an interview, if required. If this is the case, you will be advised the time and place for the interview.<br><br>

Medical Examination - The Canadian High Commission - New Delhi will issue medical instructions. Students must pass a medical examination from a Panel Doctor only. If the student is found to be in good health and there are no other concerns with the application, the Visa and Student Authorization will be issued.<br><br>

<b>What are the requirements I must meet?</b><br>
You must show the officer that you meet the requirements of the Canadian Immigration and Refugee Protection Act and Regulations and that you will be in Canada for a temporary stay. You must also:<br>
• satisfy an officer that you will leave Canada at the end of your studies;<br>
• have been accepted by an educational institution;<br>
• prove that you have enough money during your stay in Canada to pay for;<br>
• tuition fees;<br>
• living expenses for yourself and accompanying family members; and<br>
• return transportation for yourself and accompanying family members;<br>
• be law abiding and have no record of criminal activity (you may be asked to provide a Police Clearance Certificate);<br>
• not be a risk to the security of Canada;<br>
• produce any additional documents requested by the officer to establish your admissibility;<br>
• complete a medical examination, if required.<br><br>

<b>Documents required towards a complete visa application</b><br>
• Study Permit Application form.<br>
• Personal Information Form for students and postdoctoral researchers.<br>
• Letter of acceptance from Canadian University, College or Technical Institute.<br>
• Certificate of Acceptance from the province of Quebec (Only if you plan to study in Quebec)<br>
• TOEFL / IELTS score, if applicable<br>
• Passport, valid for at least one year, from the proposed date of entry into Canada.<br>
• Visa fee to be payable by Bank draft<br>
• Copies of Academic Mark sheets and Certificates<br>
• Work Experience Certificates, if applicable.<br>
• Proof of payment of fees, if applicable<br>
• Proof of Funds - Applicants must demonstrate that they have sufficient liquid assets to pay the tuition fees, living expenses and travelling cost without engaging in employment in Canada. Students must demonstrate that they have at least Cdn$10,000 plus the cost of tuition for a twelve-month period, plus the cost of travelling to and fro from Canada. Even Bank Loans are accepted.<br>
• Letter from the sponsor<br>
• Income Tax Papers of the sponsor<br><br>

<b>What documents do I need to apply for a study permit?</b><br>
Complete the application form, and include the documents listed below.<br>
<i>Important: Although the documents listed below are normally needed in support of your application, local requirements may also apply. You must satisfy an officer that you will leave Canada. Visit the local website of the visa office responsible for your area or contact their office to verify all required documents, before submitting your application.</i>
<b>1. Proof of acceptance</b><br>
• for attendance at a university, college or technical institution, a letter from the educational institution to show:<br>
o the name of the institution;<br>
o confirmation of your acceptance and/or registration as a student;<br>
o the course of study;<br>
o the duration of the academic program; and<br>
o the latest date you may register.<br><br>
• for attendance at a primary or secondary school, a letter from the school board having jurisdiction for the school you are attending (or for private schools, a letter from the school itself), indicating:<br>
o the name of the school;<br>
o the level of study; and<br>
o the duration of the course.<br>
<b>2. Proof of identity</b><br>
• a valid passport or travel document that guarantees re-entry to the country that issued it (citizens and permanent residents of the United States, St. Pierre and Miquelon, and Greenland do not require a passport but do require proof of status and citizenship such as a national identity card or an alien registration card); and<br>
• six photos of you and your accompanying family members (see Appendix B Photo Specifications for instructions).<br><br>
<b>3. Proof of financial support</b><br>
• evidence that you can support yourself and accompanying family members while you study in Canada. Such evidence may include:<br>
o proof of a Canadian bank account in your name if money has been transferred to Canada;<br>
o your bank statements for the past four months;<br>
o a bank draft in convertible currency;<br>
o proof of payment of tuition and residence fees;<br>
• for those with a scholarship or those with a Canadian funded educational program: proof of funding paid from within Canada.<br>
• if foreign exchange control measures exist in your country, you must provide proof that you will be permitted by the exchange control authorities of your country to export funds for all of your expenses.<br><br><br>

<b>Paying the Fees</b><br>
You must pay a processing fee when you submit an application for a study permit. Contact the local website of the visa office responsible for your area, for information on fees and methods of payments. Visa offices cannot accept fee payment receipts from banks in Canada.<br><br>

<i>Note: Personal cheques are not acceptable methods of payment.Your application will be reviewed to ensure it has been completed correctly and contains all of the required documents for processing.After reviewing your application, an officer will decide if an interview is necessary. If so, the officer will inform you of the time and place.</i><br><br>

If your application is approved, you will receive a letter of introduction confirming the approval. This letter is not your study permit. When you arrive in Canada, you must show this letter to a Canadian officer at the port of entry. The officer will determine whether you may enter Canada and how long you may stay. You will be issued a study permit at this time. You must leave Canada on or before the date on your study permit, set by the officer or have your status extended by an officer in Canada.<br><br>

