<b>Education System in Ireland</b><br><br>
Highest Educational Participation Rates:<br> Ireland has a long and honourable tradition in education. As a result of a sustained investment in this area Ireland now has one of the highest educational participation rates in the world.<br><br>

Demand for Ireland Graduates:<br> This dynamic, educated population has made its mark at home and abroad with international companies looking to Ireland when hiring graduates for top class positions.<br><br>

Education Policy: <br>Responsibility for education lies within the Department of Education and Skills. It administers all aspects of education policy including curricula, syllabi and national examinations. Attendance at full time education is compulsory in Ireland from six to fifteen years of age and is free in the majority of schools, and at undergraduate third-level. Education is considered a fundamental right under our constitution.
<br><br>
Levels:<br> The Irish Education System was traditionally divided into three basis levels.
<br>
• Primary (8 years)<br>
• Secondary (5 or 6 years)<br>
• Higher Education
<br><br>
They offer a wide range of opportunities from post-secondary courses, to vocational and technical training, to full degree and the highest post-graduate levels.
<br><br>
The focus has now expanded to include pre-school education and adult and further education as the concept of lifelong learning. This is reflected in the education opportunities available within the Irish education system.

