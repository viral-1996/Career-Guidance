<b>Scholarship's</b><br>
<p>
Australian universities offer a range of postgraduate scholarships to international students predominantly targeting Doctoral and Masters Degrees by research.<br><br>

The course of study and conditions of financial support is determined by the individual university. For example a university might offer to pay for the final year of study for the student who achieves the best results in a particular academic course.<br><br>

Some scholarships cover tuition and provides a stipend while other scholarships may be worth a few thousand dollars. Some universities  reserve a few scholarships only for international students. The Australian Government Education Department website can link you to the relevant university to find out more about scholarships available. In any case, you must speak to the university directly.<br><br>

