<b><h3><u>Country Overview Ireland</h3></u></b>
A soft green quilt speckled with sleepy villages and culturally vibrant cities, and framed by a ribbon of rugged coastline and sandy bays, Ireland has both beauty and brains.Ireland offers much for outdoor enthusiasts, from bracing hikes in Killarney National Park to surfing at Lahnich Beach, while history fans can tickle their interests with bucolic walks, which pass by stone cottages or prehistoric monuments, such as the burial tombs of the Boyne Valley.<br><br>

Ireland's urban centre of Cork and Dublin have cultural sway around the world; Dublin, the country’s dynamic capital, has utterly transformed over the last 20 years, and today is a luxury destination with upmarket shops and electric nightlife.From oysters fresh from Galway Bay, to chunks of black pudding prepared by one of Cork's finest butchers, Ireland's mealtimes are as alluring as its backdrop. As evening draws in, many pubs enjoy impromptu fiddle and song sessions, while highly sociable chat bubbles away over frothy real ales, smoky whiskies, and, of course, a hearty swill of Guinness.<br><br>

<b>Culture and History:</b><br>
An unswerving commitment to Catholicism on the part of the majority, and a frequent uncertainty and instability governing Anglo-Irish relations, are key features of Irish history.
<br><br>
A war between the Irish chieftains and the Vikings first led to the involvement of the English: Richard of Clare, Earl of Pembroke (nicknamed Strongbow), was invited by one the chieftains to support his claims, but instead Strongbow conquered almost the entire country with only a tiny force of archers and mounted knights in 1169-70.Many Norman families moved across the Irish Sea, effectively colonising the country. Repeated and largely unsuccessful efforts from the 14th century onwards were made to bring the island under control. The turbulent and increasingly polarised political life of Ireland took a new twist after the English Civil War, when the Irish rose in favour of the deposed monarchy in 1649. The victorious Oliver Cromwell led an army across the Irish Sea and the rebellion was ruthlessly put down.
<br><br>
Recent political agenda has been dominated the challenge to the orthodox morality of the Catholic Church, especially on the contentious issues of abortion and divorce. Equally contentious is the future of Northern Ireland. Some in Dublin believe that the lowering of barriers between countries, which is a key objective of European Union, will eventually bring about conditions where there is little difference between North and South. Yet many in the North remain deeply suspicious of Dublin's role, and are disinclined to accept anything which may bring North and South closer together.
<br><br>
<b>Social Conventions:</b><br>
The Irish are renowned as an affable, easy-going people, whose conversation is liberally dosed with humour, irreverence and - if you listen carefully - a beal bocht, an Irish expression that translates as 'the poor mouth'. It refers to a kind of modesty where the speakers are careful to talk themselves down and never reveal how well he or she is actually doing. In short, the Irish don't really like a showoff.
<br><br>
The best place to observe and engage with the Irish is in the pub, still the centre of all social interaction in Ireland, especially in smaller communities throughout the country. A meal in an Irish home is usually a substantial affair and guests will eat well. Dinner is the main meal of the day and is now eaten in the evening. Even in cities there is less formal wear than in most European countries and casual dress is widely acceptable as in keeping with a largely agricultural community. Women, however, often dress up for smart restaurants and social functions. Handshaking is usual, and modes of address will often be informal. Smoking is banned in all public enclosed/working spaces, including pubs, bars and restaurants.
