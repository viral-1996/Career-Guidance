<b>Why Study in Ireland</b><br><br>

Ireland’s worldwide reputation for high quality education is built on the solid foundation of commitment to excellence. Ireland is a beautiful island, combining contemporary modern cities with an unspoilt countryside, cityscapes steeped in history and a rich natural habitat. Renowned for friendliness, our safe English-speaking country offers the warmest of welcomes to students from all over the world.<br><br>

• Safe - Ireland was voted by Lonely Planet as the world’s friendliest country in 2008 and 2010 and was ranked 12th in the 2009 Global Peace Index. The island’s varied environment is ideal for many outdoor pursuits such as climbing, water sports and all kinds of ball and team sports.<br><br>
• Language - Ireland, an English-speaking country, has close cultural, economic, and educational links with the English-speaking world, especially with the UK and with the USA.<br><br>
• Recognition - Irish qualifications are recognised for excellence world-wide. Students can choose from an extensive range of courses to meet their needs in highly respected business schools, centres of scientific and technology excellence as well as renowned language, humanities and arts faculties.<br><br>
• Learning environment - Dedicated international offices work closely with academic, administrative and specialist staff to fully support students throughout their time in Ireland. Through these support structures, students receive direct access to information, facilities, services and staff.<br><br>
• Distinguished Graduates - Ireland’s graduates are innovators in their fields, leaders in their communities and ambassadors for excellence all around the world. Qualifications earned and connections made in Ireland deliver a passport to success.<br><br>
• Employment Opportunities - Companies who require a skilled, educated and highly capable workforce to drive their success choose to locate in Ireland. Ireland has welcomed Google, Facebook, Pfizer, Apple, Intel to name just a few — all of whom chose Ireland as their European base.<br><br><br><br>
