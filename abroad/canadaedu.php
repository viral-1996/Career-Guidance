 <b>Education SystemEducation System in Canada</b><br>

The Canadian education system comprises of<br><br>
<b>Elementary / Secondary Education:</b><br>
Preschool programs or Kindergartens, operated by local school authorities, provide pre-elementary education for 4-5 year-olds. Kindergarten programs are offered in elementary schools in all provinces and territories. The Grade organization varies among the ministries / departments of education. In some areas, it involves Kindergarten to grade 8 (elementary grades) and grades 9-12 (secondary level). Most school systems have an intermediate level of school - junior high school or middle school. Following elementary or middle level grades, school children proceed to secondary school, high school, or senior high school, where they continue to grade 12 (grade 11 in Quebec). Curriculum at the secondary level includes both academic and vocational programs. Secondary school diplomas are granted to students who pass the compulsory and optional courses of their programs.<br><br>

Canada is a confederation of ten provinces and three territories and under the Canadian constitution each of the provinces and territories have their own responsibility for education. As a result, Canada has no national or federal department of education. Public education is under the jurisdiction of each of the ten provinces and three territories. Although there are many similarities in the 13 education systems across Canada, each reflects the diversity of its own regional history, culture, and geography. Admission requirements, program and course offerings, and fees vary from one institution to another. However, standards across the country are uniformly high.<br><br>

The Canadian education system encompasses both publicly funded and private schools, from kindergarten through to university. The academic year generally runs from September to April.<br><br>

<b>Universities & University Colleges:</b><br>
Canada has a large selection of universities and university colleges. Canadian universities are largely publicly funded; as a result they are of a consistently high quality, regardless of location or area of study. As well, they all retain a high degree of academic autonomy. They offer a broad range of courses and a full range of degrees from undergraduate to doctorates, and can also offer certificates and professional degrees. The university year usually runs from September to May. Some universities are on a semester or trimester system, with all courses available even in the summer. University Colleges offers university degrees as well as college diploma and certificate programs.<br><br>

<b>Community Colleges:</b><br>
The Community Colleges offer certificate and diploma programs in career and vocational
training. College-level institutions may also be known as colleges of applied arts and technology, technology institutes. There are about 150 Community Colleges in Canada.<br><br>

<b>Career Colleges:</b><br>
A technical/career college is a privately owned and operated school with the main objective of preparing students for the job market after a short period of instruction. The emphasis at technical/career colleges is on practical skills over a broad range of programs. They may specialize in specific areas such as business, computers and secretarial skills. Although privately owned, these schools are provincially approved and regulated, ensuring that program standards and quality are maintained. Career colleges have highly competitive fee structures.<br><br><br>
