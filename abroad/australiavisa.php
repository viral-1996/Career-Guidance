<strong>*Procedures are fine tuned by the Embassy regularly. For latest updates feel free to get in touch with us.</strong><br>
<p>
TT Services has been appointed to collect visa applications on behalf of the Australian Government Visa Office in New Delhi. All visa applications from all states in India and Nepal should be lodged at TT Services Collection Centers. It is still advisable to lodge your application at the nearest TT Services Collection Centre. There will be a minimal fee of INR 450, which would be applicable towards the TT services. This amount could be paid in cash. <br><br>

<b>DOCUMENTS REQUIRED TOWARDS A COMPLETE VISA APPLICATION</b><br>

Documents to be submitted:<br> 
1. 157A - Application form<br>
Link to Download - <a href="http://www.immi.gov.au/allforms/pdf/157a.pdf">http://www.immi.gov.au/allforms/pdf/157a.pdf</a><br>
2. Supplementary Questionnaire<br>
3, Authorisation form (If required)<br>
4. Relatives Information Form<br>
5. Additional Information requests Form<br><br>

<b>STUDENT VISA APPLICATION FEES AND PROCESSING TIME</b><br>
Fees need to be paid by making a Demand Draft favoring <b>"AUSTRALIAN HIGH COMMISSION, NEW DELHI.”</b>

An application for the Student visa should be made at least 2-3 months prior to the course commencement date to allow sufficient processing time at the Australian High Commission.<br><br>

<b>VISA APPLICATION PROCEDURES:</b><br>
Students wanting to study in Australia can apply for a student visa after they have received a letter of offer or electronic confirmation of enrolment (eCoE) in relation to one or more full-time courses of study. However, it is recommended that you do not enroll in your course or obtain an electronic Confirmation of Enrolment (eCoE) until you have been advised to do so by your local Australian Government office. <br><br>

<b>STEPS INVOLVED IN THE VISA PROCESS:</b><br> 

<i>STEP 1:</i><br>
Lodge your application with a copy of the Letter of Offer from your education provider and all relevant forms and documents as per the checklist attached. You must not pay the course fees or have medical tests until you receive this letter. However, there are students who make the tuition fee payment before the Pre Visa Assessment is completed for faster processing of the application.<br><br>

<i>STEP 2:</i><br>
The department completes a pre-visa assessment of your application. A letter will be sent advising whether you are considered a 'genuine' student. If you are assessed as a non-genuine student, your visa application will be refused. If you are assessed as genuine, see Step 3.<br><br>

<i>STEP 3:</i><br>
Once you receive the letter, you can: <br>
-have your medical tests (if required)<br> 
-pay your fees if not paid <br>
-obtain and send to the department your Confirmation of Enrolment.<br><br>

<i>STEP 4:</i><br>
The department makes a final assessment of your application.<br>
<b>Note: If you are assessed as a genuine student, you must also meet all other requirements to be granted a student visa.</b><br><br>
  
