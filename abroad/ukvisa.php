<b>Visa's For Studying in  United Kingdom</b><br>
<strong>*Procedures are fine tuned by the Embassy regularly. For latest updates feel free to get in touch with us.</strong>
<p>
Visa applications have to be made on IM2A forms, which are available free of cost at the British Embassies. This form has to be filled up by the student and submitted to the embassy, along with the Visa application fee and the required documents as mentioned below.<br><br>

<b>Immigration Rules for Students:</b><br>
• You must intend to study at a publicly funded university or college, a bonafide private institution or a fee-paying independent school.<br>
• You must  be able to and intend to follow a full time degree course, or a weekday, full time course at a single institution involving at least 15 hours of organized, daytime study per week.<br>
• You must be able to pay for your course and the living expenses of your  husband or wife and children (if they are with you) without working in  UK or claiming public funds.<br>
• You must not intend to work in UK unless you are accepted for a  course lasting longer than six months, when you may work part-time or during vacations.<br>
• You must intend to leave UK at the end of your studies.<br><br>

<b>Documents Required For a Student Visa:</b><br>
• A letter of acceptance on the course<br>
This will be a letter from your institution confirming that a place has been offered to you and that the course is full-time as defined by the immigration law. The letter should state how long the course will last. Where your course is more advanced and specialized, the letter should also state what level of English is needed for the course (giving minimum test marks, if appropriate) and confirm that you satisfy this requirement.<br>

• Evidence that you can pay the course fees and support yourself and your family, if applicable<br>
Depending on your circumstances, this could include evidence of government sponsorship, a letter from a sponsor in UK confirming they can support you, together with evidence that they can do this, and/or your own bank statements. You should also provide a letter from your place of study stating the full cost of the course, what arrangements are acceptable for payment and whether you have already paid fees or a deposit.<br><br>

<b>Accommodation:</b><br>
Although the immigration rules do not require unaccompanied students to show that accommodation has been arranged, your place of study may have given you information about its availability, and you should bring this to the attention of the ECO. If your family is coming with you to the UK, you will need to show that there will adequate accommodation for them. If your place of study cannot confirm that family accommodation will be available, you may have to consider travelling to the UK alone and making arrangements for your family to join you when you have found somewhere to live.<br><br>

<b>The Interview</b><br>
Interview is a part of your visa application process.<br>

The interview is to give you the opportunity to clarify for the ECO certain parts of your application. The ECO will be aiming to satisfy him or herself that your study plans are genuine and workable.<br>

The interview is a normal part of the process, but you may feel quite nervous before it and find it an uncomfortable experience. ECO’s are trained professionals who will try to make the process as painless as possible, but the questions are necessarily very direct and personal.<br><br>

You should prepare for such an interview carefully. In particular:<br>
• Be familiar with all your study plans; why you chose the particular institution and course; how the course will help your future career;<br>
• Be clear about the cost of living and the course and your finances: do you have enough money to complete the course? Can you prove it?<br>
• Be as clear as you can about your likely future career in your country: what are you going to do on your return.<br><br>
