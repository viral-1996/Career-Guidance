<b>Admission Procedures</b>
<p> 
For undergraduate studies in Germany,international students need to complete a one-year 'bridge program'. After completing this program, students from most overseas countries need topass an assessment test for admission.However, students who have cleared some of the well-known entrance examinations in their country might be exempted from this assessment test.<br><br>

This, however, depends on the various guidelines that German institutions follow.Students who have successfully completed two years of undergraduate studies are also exempted from the assessment test.However, they need to verify their aptitude in the German language.<br><br>

For admission in postgraduate courses, students usually requirea four-year bachelor's degreebut many Institutions also accept a three-year degree.For PhD courses, the admission requirements in Germany are similar to many other countries. For example, international students require a Master's Degree and should identify a professor or academician in Germany as their doctoral guide.<br><br>

Students have to apply directly to the universities;forms can be collected from the international offices of the concerned universities. The completed applications, along with the required documents, have to be sent back to the university. Students also need to provide authenticated copies of certificates validating their knowledge of German, if required.<br><br>

Following are the entrance requirements in Germany for international students:<br>
1)Students interested in full time studies in Germany have to apply and get a student visa before the program begins.<br>
2)Previous academic records are usually the basis for admissions in German universities.<br>
3)GMAT score is compulsory for some of the management courses, while some engineering programs require GRE score.<br>
4)For study programs with English as the medium of instruction, the TOEFL is a standard test that is required.<br><br>
Further details can be obtained from the university websites.
