<b>Scholarship's</b><br>
<p>UK Universities are generally government funded which mostly means limited funds available for the university to disburse among students as bursaries and scholarships. Still most universities do have certain awards which are given to students based on their merits as well as performance during the course.<br><br>

Some of the UK universities also have special awards for Indian students besides the ones which individual departments may offer to students coming there to pursue their further studies. Another important source of scholarship are the British Government sponsored awards like the Chevening Scholarship Commonwealth Scholarship, DFID scholarship. These are extremely competitive and the deadlines are much in advance of the session.</p>
