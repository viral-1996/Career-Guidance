 <b>Education System in Germany</b><br>
<p>
Germany has on offer a wide variety of courses in over 350 state and private universities, in the two-tier international structure of bachelor's and master's degrees. In addition, academic programs in Germany are not restricted only to the German medium. More than 500 International Degree Programmes (IDP) offer courses completely or partially taught in English.<br><br>
Choosing a course in Germany might actually be quite easy as there are only three broad types to choose from:<br>
1)Universities<br>
2)Universities of applied sciences<Br>
3)Art, film and music colleges<br><br>
Moreover, there are technical and other specialized universities. The duration of higher education in Germany ranges from 6-8 years depending on the subject and level of study. To acquire a Ph.D. (doctoral) degree, students need to pass a final examination.<br>
In Germany, the state controls the entire education system. Another significant component of the German education system is the "Adult Education" system, which is run by government and non-government institutions.
</p>
