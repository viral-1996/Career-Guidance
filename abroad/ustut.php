<b>Tuition and Living Cost in United States</b>
<p>
US Universities fall have two major categories - public (state supported), and private (independent) institutions.

Public Schools are inexpensive compared to Private ones. Please note that the cost of a program in a US school does not necessarily affect its quality. Tuition expenses at state schools are based on nonresident costs for International students.<br>

The tuition fee is different for different universities and varies widely with courses. It can vary from as low as $ 5000 a year for state universities to as much as $ 30000 per annum for some private universities.<br>

<pre style="font-size:15px;">
University Type          Average Tuition Fees (annual in U.S. Dollars)
Private Institutions              $15000 - $ 25,000
State Institutions                 $10000 - $20000
</pre><br><br>

<strong>Living costs can differ as per each individual’s lifestyle. A rough indication can be as such
<pre style="font-size:20px;">Rent - $600 to $1100
Groceries - $120
Phone - $100
Sundry - $150</pre>
</strong>
<b>• Above costs is a rough indication and may vary with actual.</b></p>
